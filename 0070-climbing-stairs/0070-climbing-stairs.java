class Solution {
    public int climbStairs(int n) {
        int[]dp = new int[n+1];//n=5, array of size 5 - 0,1,2,3,4
        if(n<=2) return n;
        dp[1]=1;
        dp[2]=2;
        int i=3;
        while(i<=n){
            dp[i]=dp[i-2]+dp[i-1];//[0,1,2,3,5,8] dp[5]=5+3
            i++;
        }
        return dp[n];
    }
    
}

